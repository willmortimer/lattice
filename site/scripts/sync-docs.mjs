#!/usr/bin/env node
/**
 * sync-docs.mjs
 *
 * Copies the repository's architecture documentation (../../docs relative to the
 * site package) into src/content/docs/ so Starlight can render it, WITHOUT
 * hand-editing the source. For every file it:
 *
 *   - derives a Starlight `title` from the first `# H1` heading
 *   - strips that H1 from the body (Starlight renders the title itself,
 *     so keeping it would produce a duplicate H1)
 *   - rewrites relative `*.md` links so they resolve as Starlight routes
 *     (e.g. `./01-product-vision.md` -> `/docs/01-product-vision/`)
 *   - leaves fenced code blocks untouched (example links stay as written)
 *
 * It also emits `src/generated/sidebar.json`, an ordered sidebar description
 * consumed by astro.config.mjs, so the navigation labels/order are fully
 * controlled rather than autogenerated.
 *
 * Plain Node, zero dependencies.
 */

import { readFileSync, writeFileSync, mkdirSync, readdirSync, rmSync, existsSync, statSync } from 'node:fs';
import { dirname, join, resolve, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SITE_ROOT = resolve(__dirname, '..');
const DOCS_SRC = resolve(SITE_ROOT, '..', 'docs');
// Starlight owns the site root, so to serve docs under /docs/ we nest the
// content one directory deeper: files in src/content/docs/docs/ receive a
// `docs/...` slug -> route `/docs/...`, leaving `/` free for the landing page.
const OUT_DIR = resolve(SITE_ROOT, 'src', 'content', 'docs', 'docs');
const GEN_DIR = resolve(SITE_ROOT, 'src', 'generated');

const DOCS_BASE = '/docs';

// Files that should not be published as documentation pages.
const SKIP = new Set(['MANIFEST.md']);

/** Recursively collect `.md` files (posix-relative paths) under a directory. */
function collectMarkdown(dir, base = dir) {
  const out = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const abs = join(dir, entry.name);
    if (entry.isDirectory()) {
      out.push(...collectMarkdown(abs, base));
    } else if (entry.isFile() && entry.name.endsWith('.md')) {
      out.push(relative(base, abs).split('\\').join('/'));
    }
  }
  return out;
}

/**
 * Map a source-relative markdown path to its published output info.
 * Returns { outRel, route } or null when the file is skipped.
 *
 *   00-overview.md              -> content/docs/index.md          @ /docs/
 *   01-product-vision.md        -> content/docs/01-.../index? no  @ /docs/01-product-vision/
 *   decisions/README.md         -> content/docs/decisions/index.md@ /docs/decisions/
 *   decisions/0001-foo.md       -> content/docs/decisions/0001-foo.md @ /docs/decisions/0001-foo/
 */
function planFor(relPath) {
  if (SKIP.has(relPath)) return null;

  if (relPath === '00-overview.md') {
    return { outRel: 'index.md', route: `${DOCS_BASE}/` };
  }
  if (relPath === 'decisions/README.md') {
    return { outRel: 'decisions/index.md', route: `${DOCS_BASE}/decisions/` };
  }

  const noExt = relPath.replace(/\.md$/, '');
  return { outRel: relPath, route: `${DOCS_BASE}/${noExt}/` };
}

/** Extract the first H1 as title and return { title, body } with the H1 removed. */
function extractTitle(source, fallback) {
  const lines = source.split('\n');
  let title = fallback;
  let removed = false;
  const kept = [];
  for (const line of lines) {
    if (!removed) {
      const m = /^#\s+(.+?)\s*$/.exec(line);
      if (m) {
        title = m[1].trim();
        removed = true;
        continue; // drop the H1 line
      }
    }
    kept.push(line);
  }
  // Trim leading blank lines left behind by the removed heading.
  while (kept.length && kept[0].trim() === '') kept.shift();
  return { title, body: kept.join('\n') };
}

/** YAML-safe double-quoted scalar. */
function yamlQuote(s) {
  return '"' + String(s).replace(/\\/g, '\\\\').replace(/"/g, '\\"') + '"';
}

/**
 * Rewrite markdown links whose targets are relative `.md` files, using the
 * route map. `sourceRel` is the source path of the file being processed, used
 * to resolve relative targets (notably ADRs linking siblings by bare name).
 * Fenced code blocks are skipped so illustrative example links stay verbatim.
 */
function rewriteLinks(body, sourceRel, routeByOutRel, routeBySource) {
  const sourceDir = sourceRel.includes('/') ? sourceRel.slice(0, sourceRel.lastIndexOf('/')) : '';

  // Split on fenced code blocks (``` or ~~~). Even indices are prose.
  const segments = body.split(/(^[ \t]*(?:```|~~~)[\s\S]*?^[ \t]*(?:```|~~~)[ \t]*$)/gm);

  const linkRe = /\[([^\]]*)\]\(([^)\s]+)\)/g;

  const resolved = segments.map((seg, i) => {
    if (i % 2 === 1) return seg; // code fence, leave alone
    return seg.replace(linkRe, (whole, text, target) => {
      // Ignore external, anchor-only, mail, and non-markdown targets.
      if (/^(https?:|mailto:|#|\/)/.test(target)) return whole;
      const hashIdx = target.indexOf('#');
      const pathPart = hashIdx >= 0 ? target.slice(0, hashIdx) : target;
      const anchor = hashIdx >= 0 ? target.slice(hashIdx) : '';
      if (!pathPart.endsWith('.md')) return whole;

      // Normalise the target relative to the source file's directory.
      const joined = sourceDir ? `${sourceDir}/${pathPart}` : pathPart;
      const norm = normalizePosix(joined);

      const route = routeBySource.get(norm);
      if (route) return `[${text}](${route}${anchor})`;

      // Unmapped .md target (e.g. skipped MANIFEST.md): drop the link, keep text.
      return text;
    });
  });

  return resolved.join('');
}

/** Resolve `.`/`..` segments in a posix-style path. */
function normalizePosix(p) {
  const parts = p.split('/');
  const stack = [];
  for (const part of parts) {
    if (part === '' || part === '.') continue;
    if (part === '..') stack.pop();
    else stack.push(part);
  }
  return stack.join('/');
}

function humanTitle(title) {
  return title;
}

function main() {
  if (!existsSync(DOCS_SRC) || !statSync(DOCS_SRC).isDirectory()) {
    console.error(`[sync-docs] source docs directory not found: ${DOCS_SRC}`);
    process.exit(1);
  }

  const files = collectMarkdown(DOCS_SRC);

  // Build the source-path -> route map first so links can be resolved.
  const routeBySource = new Map(); // e.g. "01-product-vision.md" -> "/docs/01-product-vision/"
  for (const relPath of files) {
    const plan = planFor(relPath);
    if (plan) routeBySource.set(relPath, plan.route);
  }

  // Clean output dir (fully generated, never committed).
  rmSync(OUT_DIR, { recursive: true, force: true });
  mkdirSync(OUT_DIR, { recursive: true });

  const emitted = []; // { sourceRel, outRel, route, title }

  for (const relPath of files) {
    const plan = planFor(relPath);
    if (!plan) continue;

    const raw = readFileSync(join(DOCS_SRC, relPath), 'utf8');
    const fallback = relPath.replace(/\.md$/, '');
    const { title, body } = extractTitle(raw, fallback);
    const rewritten = rewriteLinks(body, relPath, routeBySource, routeBySource);

    const frontmatter = ['---', `title: ${yamlQuote(humanTitle(title))}`];
    // Root index gets a friendlier nav label + hero-less description.
    frontmatter.push('---', '');

    const outPath = join(OUT_DIR, plan.outRel);
    mkdirSync(dirname(outPath), { recursive: true });
    writeFileSync(outPath, frontmatter.join('\n') + rewritten.replace(/\s*$/, '') + '\n', 'utf8');

    emitted.push({ sourceRel: relPath, outRel: plan.outRel, route: plan.route, title });
  }

  // Build ordered sidebar description.
  const byRoute = (a, b) => a.sourceRel.localeCompare(b.sourceRel, 'en');
  const overview = emitted.find((e) => e.sourceRel === '00-overview.md');
  const mainDocs = emitted
    .filter((e) => !e.sourceRel.startsWith('decisions/') && e.sourceRel !== '00-overview.md')
    .sort(byRoute);
  const adrIndex = emitted.find((e) => e.sourceRel === 'decisions/README.md');
  const adrs = emitted
    .filter((e) => e.sourceRel.startsWith('decisions/') && e.sourceRel !== 'decisions/README.md')
    .sort(byRoute);

  const sidebar = [];
  if (overview) sidebar.push({ label: 'Overview', link: overview.route });
  sidebar.push({
    label: 'Architecture',
    collapsed: false,
    items: mainDocs.map((e) => ({ label: e.title, link: e.route })),
  });
  const adrItems = [];
  if (adrIndex) adrItems.push({ label: 'All decisions', link: adrIndex.route });
  for (const e of adrs) adrItems.push({ label: e.title, link: e.route });
  sidebar.push({ label: 'Architecture Decisions', collapsed: true, items: adrItems });

  mkdirSync(GEN_DIR, { recursive: true });
  writeFileSync(join(GEN_DIR, 'sidebar.json'), JSON.stringify(sidebar, null, 2) + '\n', 'utf8');

  console.log(
    `[sync-docs] wrote ${emitted.length} pages to ${relative(SITE_ROOT, OUT_DIR)} ` +
      `(${mainDocs.length} architecture, ${adrs.length} ADRs) and sidebar.json`
  );
}

main();
